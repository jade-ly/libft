#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	size_t	size;
	size_t	i;

	size = ft_strlen(s);
	dst = malloc(sizeof(char) * (size + 1));
	if (!dst)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
